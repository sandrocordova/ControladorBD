    private void anteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_anteriorActionPerformed
        if (indice > 0) {
            indice--;
            ID.setText(lista.get(indice - 1).getId() + "");
            nombres.setText(lista.get(indice - 1).getNombre());
            apellidos.setText(lista.get(indice - 1).getApellido());
            email.setText(lista.get(indice - 1).getEmail());
            telefono.setText(lista.get(indice - 1).getTelefono());
            siguiente.setEnabled(true);
        }
        if (indice == 0) {

            ID.setText("");
            apellidos.setText("");
            nombres.setText("");
            email.setText("");
            telefono.setText("");
        }
        if (indice - 1 == 0) {
            anterior.setEnabled(false);
        }

        numeroAnterior.setText(indice + "");
        System.out.println("anterior" + indice);
        // TODO add your handling code here:
    }//GEN-LAST:event_anteriorActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (!buscarApellido.getText().equalsIgnoreCase("")) {
            List<persona> busqueda = cb.getPersonaApellido(buscarApellido.getText());
            lista = busqueda;
            indice = 0;

            if (indice < lista.size()) {
                siguiente.setEnabled(true);
                anterior.setEnabled(true);
                ID.setText(lista.get(indice).getId() + "");
                nombres.setText(lista.get(indice).getNombre());
                apellidos.setText(lista.get(indice).getApellido());
                email.setText(lista.get(indice).getEmail());
                telefono.setText(lista.get(indice).getTelefono());
                indice++;
                anterior.setEnabled(false);
                if (indice == lista.size()) {
                    siguiente.setEnabled(false);
                }
            }
            if (lista.size() == 0) {
                siguiente.setEnabled(false);
                anterior.setEnabled(false);
                ID.setText("");
                apellidos.setText("");
                nombres.setText("");
                email.setText("");
                telefono.setText("");
            }

            numeroSiguiente.setText(lista.size() + "");
            numeroAnterior.setText(indice + "");
            buscarApellido.setText("");
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

    private void siguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_siguienteActionPerformed

        if (indice < lista.size()) {

            ID.setText(lista.get(indice).getId() + "");
            nombres.setText(lista.get(indice).getNombre());
            apellidos.setText(lista.get(indice).getApellido());
            email.setText(lista.get(indice).getEmail());
            telefono.setText(lista.get(indice).getTelefono());
            indice++;
            anterior.setEnabled(true);
        }
        if (indice == lista.size()) {
            siguiente.setEnabled(false);
        } else {
            siguiente.setEnabled(true);
        }
        numeroAnterior.setText(indice + "");
        System.out.println("siguiente" + indice);// TODO add your handling code here:
    }//GEN-LAST:event_siguienteActionPerformed

