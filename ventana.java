    int indiceInsertar = 0;
    private void insertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertarActionPerformed

        siguiente.setEnabled(false);
        anterior.setEnabled(false);
        numeroAnterior.setText("");
        numeroSiguiente.setText("");
        ID.setText((cb.getPersona().size() + 1) + "");
        if (indiceInsertar == 0) {
            apellidos.setText("");
            nombres.setText("");
            email.setText("");
            telefono.setText("");
            indiceInsertar++;
        } else {
            if (nombres.getText().equalsIgnoreCase("") || apellidos.getText().equalsIgnoreCase("")
                    || email.getText().equalsIgnoreCase("") || telefono.getText().equalsIgnoreCase("")) {
                JOptionPane.showMessageDialog(null, "Asegurese de aber llenado todos los campos", "ERROR", JOptionPane.ERROR_MESSAGE);
            } else {
                cb.agregarPersona(nombres.getText(), apellidos.getText(), email.getText(), telefono.getText());
                apellidos.setText("");
                nombres.setText("");
                email.setText("");
                telefono.setText("");
                indiceInsertar = 0;
                indice=0;
            }
        }
        
        // TODO add your handling code here:
    }//GEN-LAST:event_insertarActionPerformed

